    public void cadSolo() { //realiza o cadastro dos dados do solo
        solo = new Solo();

        try {
            solo.setProdId(Integer.parseInt(cxProd.getText()));
            solo.setSoloId(Integer.parseInt(cxSolo.getText()));
            solo.setFosforo(Double.parseDouble(cxFosforo.getText()));
            solo.setPotassio(Double.parseDouble(cxPotassio.getText()));
            solo.setCalcio(Double.parseDouble(cxCalcio.getText()));
            solo.setMagnesio(Double.parseDouble(cxMagnesio.getText()));
            solo.setEnxofre(Double.parseDouble(cxEnxofre.getText()));
            solo.setAluminio(Double.parseDouble(cxAluminio.getText()));
            solo.setHal(Double.parseDouble(cxHal.getText()));

            if (bancoSolo.consProdCod(solo) == null) {
                bancoSolo.getBDSolo().add(solo);
                JOptionPane.showMessageDialog(null, "Dados cadastrados com sucesso!", "Cadastro OK", JOptionPane.INFORMATION_MESSAGE);
                cxProd.setText("");
                cxSolo.setText("");
                cxFosforo.setText("");
                cxPotassio.setText("");
                cxCalcio.setText("");
                cxMagnesio.setText("");
                cxEnxofre.setText("");
                cxAluminio.setText("");
                cxHal.setText("");
                cxProd.requestFocus();
                showTabSolo();
            } else {
                JOptionPane.showMessageDialog(null, "Já existe um cadastro com este código!", "ERRO Cadastro", 0);
                cxProd.setText("");
                cxProd.requestFocus();
            }

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "O Código deve ser um inteiro!", "Erro de Código", JOptionPane.ERROR_MESSAGE);

            cxProd.setText("");
            cxProd.requestFocus();
        }
    }
