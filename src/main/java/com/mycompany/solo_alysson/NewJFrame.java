    public void showTabSolo() {

        solo = new Solo();

        DefaultTableModel modTabSolo = new DefaultTableModel();

        modTabSolo = (DefaultTableModel) tabelaDados.getModel();

        modTabSolo.setRowCount(0);

        for (int posLinha = 0; posLinha < bancoSolo.getBDSolo().size(); posLinha++) {

            solo.setProdId(bancoSolo.getBDSolo().get(posLinha).getProdId());
            solo.setFosforo(bancoSolo.getBDSolo().get(posLinha).getFosforo());
            solo.setPotassio(bancoSolo.getBDSolo().get(posLinha).getPotassio());
            solo.setCalcio(bancoSolo.getBDSolo().get(posLinha).getCalcio());
            solo.setMagnesio(bancoSolo.getBDSolo().get(posLinha).getMagnesio());
            solo.setEnxofre(bancoSolo.getBDSolo().get(posLinha).getEnxofre());
            solo.setAluminio(bancoSolo.getBDSolo().get(posLinha).getAluminio());
            solo.setHal(bancoSolo.getBDSolo().get(posLinha).getHal());

            modTabSolo.insertRow(posLinha, new Object[]{solo.getProdId(),
                solo.getFosforo(), solo.getPotassio(),
                solo.getCalcio(), solo.getMagnesio(), solo.getEnxofre(),
                solo.getAluminio(), solo.getHal()});
        }
    }

